def color_gradient(text, start_color, end_color, step, start_step, end_step):
    """ 根據步驟將文本顏色從 start_color 變為 end_color (只影響指定範圍內的幀) """
    if step < start_step:
        return f"[rgb({start_color[0]},{start_color[1]},{start_color[2]})]{text}[/rgb({start_color[0]},{start_color[1]},{start_color[2]})]"
    elif step >= end_step:
        return f"[rgb({end_color[0]},{end_color[1]},{end_color[2]})]{text}[/rgb({end_color[0]},{end_color[1]},{end_color[2]})]"
    else:
        # 計算漸變比例
        progress = (step - start_step) / (end_step - start_step)
        red = int(start_color[0] + (end_color[0] - start_color[0]) * progress)
        green = int(start_color[1] + (end_color[1] - start_color[1]) * progress)
        blue = int(start_color[2] + (end_color[2] - start_color[2]) * progress)

        return f"[rgb({red},{green},{blue})]{text}[/rgb({red},{green},{blue})]"

